const body = document.body;
const langSwitch = document.querySelector('.lang-switch');
const currentLabel = document.querySelector('.lang-current');
const menuToggle = document.querySelector('.menu-toggle');
const navWrap = document.querySelector('.nav-wrap');
const navLinks = document.querySelectorAll('.nav a, .nav-actions a');

function applyLanguage(lang) {
  document.documentElement.lang = lang;
  body.dataset.lang = lang;
  currentLabel.textContent = lang.toUpperCase();
  localStorage.setItem('pcg-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });
}

langSwitch.addEventListener('click', () => {
  const nextLang = body.dataset.lang === 'de' ? 'en' : 'de';
  applyLanguage(nextLang);
});

menuToggle.addEventListener('click', () => {
  const isOpen = navWrap.classList.toggle('open');
  menuToggle.setAttribute('aria-expanded', String(isOpen));
});

navLinks.forEach((link) => {
  link.addEventListener('click', () => {
    navWrap.classList.remove('open');
    menuToggle.setAttribute('aria-expanded', 'false');
  });
});

const savedLang = localStorage.getItem('pcg-lang') || 'de';
applyLanguage(savedLang);
